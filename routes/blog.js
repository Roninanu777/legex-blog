var express=require("express"),
	middleware=require("../middleware"),
	router=express.Router(),
	Blog=require("../models/blogSchema"),
	middlewareObj=require("../middleware"),
	smartTrim=require("../helper/blog"),
    Category=require("../models/category"),
    User=require("../models/userSchema"),
	Tag=require("../models/tag"),
	formidable = require('formidable'),
	slugify = require('slugify'),
	stripHtml = require('string-strip-html'),
    _ = require('lodash'),
    { smartTrim } = require('../helper/blog'),
	{ errorHandler } = require('../helper/dbErrorHandler'),
	fs = require('fs'); 

router.post("/api/blog",middlewareObj.requireSignin,middlewareObj.adminMiddleware,(req,res)=>{
	let form = new formidable.IncomingForm();
    form.keepExtensions = true;
    form.parse(req, (err, fields, files) => {
        if (err) {
            return res.status(400).json({
                error: 'Image could not upload'
            });
        }

        const { title, body, categories, tags } = fields;

        if (!title || !title.length) {
            return res.status(400).json({
                error: 'title is required'
            });
        }

        if (!body || body.length < 200) {
            return res.status(400).json({
                error: 'Content is too short'
            });
        }

        if (!categories || categories.length === 0) {
            return res.status(400).json({
                error: 'At least one category is required'
            });
        }

        if (!tags || tags.length === 0) {
            return res.status(400).json({
                error: 'At least one tag is required'
            });
        }

        let blog = new Blog();
        blog.title = title;
        blog.body = body;
        blog.slug = slugify(title).toLowerCase();
        blog.mtitle = `${title} | ${process.env.APP_NAME}`;
        blog.mdesc = stripHtml(body.substring(0, 160));
		blog.postedBy = req.user._id;
		blog.excerpt=smartTrim(body,300,' ' , '...')
        // categories and tags
        let arrayOfCategories = categories && categories.split(',');
		let arrayOfTags = tags && tags.split(',');
		blog.categories=arrayOfCategories;
		blog.tags=arrayOfTags;

        if (files.photo) {
            if (files.photo.size > 10000000) {
                return res.status(400).json({
                    error: 'Image should be less then 1mb in size'
                });
            }
            blog.photo.data = fs.readFileSync(files.photo.path);
            blog.photo.contentType = files.photo.type;
        }

        blog.save((err, result) => {
            if (err) {
                return res.status(400).json({
                    error: errorHandler(err)
                });
            }
             res.json(result);
        });
    });
})

